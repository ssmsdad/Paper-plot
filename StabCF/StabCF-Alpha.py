def plot_single_metric(dataset_name, metric):
    """为单个数据集绘制指定指标随 alpha 变化的图表，并保存为 PDF 文件"""

    # values = apegnn_datasets[dataset_name][metric]
    values = ngcf_datasets[dataset_name][metric]
    # values = lightgcn_datasets[dataset_name][metric]
    x_full = list(range(1, len(values) + 1))

    fig, ax = plt.subplots(figsize=(10, 6))
    color_map = {
        "ali": "#1f77b4",
        "yelp2018": "#F5A623",
        "amazon": "#E24A4AFA"
    }
    markerfacecolor_map = {
        "ali": "#F5A623",
        "yelp2018": "#1f77b4",
        "amazon": "#B74AE2F9"
    }
    markerfacecolor = markerfacecolor_map.get(dataset_name, "#1f77b4")
    color = color_map.get(dataset_name, "#0d0d0d")
    mark_map = {
        "ali": "*",
        "yelp2018": "*",
        "amazon": "o"
    }
    marksize_map = {
        "ali": 50,
        "yelp2018": 50,
        "amazon": 50
    }
    markersize = marksize_map.get(dataset_name, 46)
    marker = mark_map.get(dataset_name, "*")
    ax.plot(x_full, values, color=color, linewidth=14)
    max_idx = values.index(max(values))
    ax.plot(x_full[max_idx], values[max_idx], marker=marker, color=color, markersize=markersize, label="max",markerfacecolor=markerfacecolor,markeredgewidth=0)

    # x轴刻度步长为5
    xticks = list(range(1, len(values) + 1, 4))
    ax.set_xticks(xticks)
    ax.set_xticklabels([str(i) for i in xticks], fontsize=32)
    ax.set_xlabel(r"$\alpha$", fontsize=50)
    ax.set_ylabel(metric, fontsize=50)

    if dataset_name == "ali":
        ax.legend(loc="upper right", fontsize=40)
    else:
        ax.legend(loc="lower right", fontsize=40)
    # ax.legend(loc="lower right", fontsize=40)
    ax.grid(True, alpha=0.3)
    ax.tick_params(axis="both", which="both", length=0)
    ax.tick_params(axis="x", labelsize=32)
    ax.tick_params(axis="y", labelsize=26)

    # plt.savefig(f"./sensitivity/alpha/apegnn-{metric}_vs_alpha_{dataset_name}.pdf", bbox_inches="tight")
    plt.savefig(f"./sensitivity/alpha/ngcf-{metric}_vs_alpha_{dataset_name}.pdf", bbox_inches="tight")
    # plt.savefig(f"./sensitivity/alpha/lightgcn-{metric}_vs_alpha_{dataset_name}.pdf", bbox_inches="tight")
    plt.close()

# 一次性绘制所有数据集和指标
# for dataset_name in apegnn_datasets.keys():
for dataset_name in ngcf_datasets.keys():
    for metric in ["Recall@20", "NDCG@20"]:
        plot_single_metric(dataset_name, metric)